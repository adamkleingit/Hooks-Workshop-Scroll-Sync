import React, { useState, useRef, useLayoutEffect } from "react";
import ReactDOM from "react-dom";
import "./styles.css";

function ScrollSync({ scrollTop, setScrollTop, children }) {
  const ref = useRef();

  useLayoutEffect(function syncScroll() {
    ref.current.scrollTop = scrollTop;
  }, [scrollTop]);

  const onScroll = (e) => {
    setScrollTop(e.target.scrollTop);
  };
  return (
    <div
      className="App"
      ref={ref}
      onScroll={onScroll}>
      {children}
    </div>
  );
}

function useScrollSync(initialValue) {
  const [scrollTop, setScrollTop] = useState(initialValue);

  return {
    scrollTop,
    setScrollTop
  }
}


